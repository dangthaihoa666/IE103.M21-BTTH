---D FUNCTION 

--1D Viết hàm tính điểm trung bình của một đề tài. Giá trị trả về là điểm trung bình ứng với mã số đề tài nhập vào.
CREATE FUNCTION F_TINHDTB (@MSDT CHAR(6))
RETURNS FLOAT 
AS
BEGIN 
	DECLARE @DIEM FLOAT
	SELECT @DIEM = (SUM(GV_HDDT.DIEM) + SUM(GV_UVDT.DIEM) + SUM(GV_PBDT.DIEM)) / (COUNT(GV_HDDT.DIEM) + COUNT(GV_UVDT.DIEM) + COUNT(GV_PBDT.DIEM))
	FROM DETAI AS DT JOIN GV_HDDT ON DT.MSDT = GV_HDDT.MSDT
	JOIN GV_UVDT ON DT.MSDT = GV_UVDT.MSDT
	JOIN GV_PBDT ON DT.MSDT = GV_PBDT.MSDT
	WHERE DT.MSDT = @MSDT
	IF (@DIEM IS NULL)
		SET @DIEM = 0
	RETURN @DIEM
END

--2D Trả về kết quả của đề tài theo MSDT nhập vào. Kết quả là DAT nếu như điểm trung bình từ 5 trở lên, và KHONGDAT nếu như điểm trung bình dưới 5.
CREATE FUNCTION F_KETQUADT (@MSDT CHAR(6))
RETURNS CHAR(10)
AS
BEGIN 
	DECLARE @DIEM FLOAT
	DECLARE @KETQUA CHAR(10)
	SELECT @DIEM = (SUM(GV_HDDT.DIEM) + SUM(GV_UVDT.DIEM) + SUM(GV_PBDT.DIEM)) / (COUNT(GV_HDDT.DIEM) + COUNT(GV_UVDT.DIEM) + COUNT(GV_PBDT.DIEM))
	FROM DETAI AS DT JOIN GV_HDDT ON DT.MSDT = GV_HDDT.MSDT
	JOIN GV_UVDT ON DT.MSDT = GV_UVDT.MSDT
	JOIN GV_PBDT ON DT.MSDT = GV_PBDT.MSDT
	WHERE DT.MSDT = @MSDT
	IF (@DIEM IS NULL)
		SET @DIEM = 0
	IF (@DIEM >= 5)
		SET @KETQUA = 'DAT'
	ELSE IF (@DIEM < 5)
		SET @KETQUA = 'KHONGDAT'
	RETURN @KETQUA
END

---E CURSOR

--Viết Cursor tính điểm trung bình cho từng đề tài. Sau đó lưu kết quả vào bảng DETAI_DIEM.
CREATE TABLE DETAI_DIEM
(
	MSDT CHAR(6) PRIMARY KEY,
	DIEMTB FLOAT
)

--Khai báo con trỏ
DECLARE p CURSOR FOR SELECT MSDT, TENDT, dbo.F_TINHDTB(MSDT) AS DIEM_TB FROM DETAI 
--Thiết lập giá trị con trỏ
DECLARE @A CHAR(6), @T NVARCHAR(30), @C FLOAT
OPEN p 
--Gán giá trị cho các biến 
---Lấy dòng đầu tiên
FETCH p INTO @A, @T, @C
---Kiểm tra có dữ liệu không, nếu có thì duyệt tiếp các dòng tiếp theo
WHILE (@@FETCH_STATUS = 0)
BEGIN
	INSERT INTO DETAI_DIEM (MSDT, DIEMTB) VALUES (@A,ROUND(@C,2))
	FETCH NEXT FROM p INTO @A, @T, @C
END
--Đóng con trỏ
CLOSE p
DEALLOCATE p
