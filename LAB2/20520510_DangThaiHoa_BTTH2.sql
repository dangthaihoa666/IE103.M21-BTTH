--- A PROCEDURE

--1A Tham số vào là MSGV, TENGV, SODT, DIACHI, MSHH, NAMHH. Trước khi insert dữ liệu cần kiểm tra MSHH đã tồn tại trong table HOCHAM chưa, nếu chưa thì trả về giá trị 0.
CREATE PROCEDURE THEM_GV @MSGV INT, @TENGV NVARCHAR(30), @SODT VARCHAR(10), @DIACHI NVARCHAR(50), @MSHH INT, @NAMHH SMALLDATETIME
AS
BEGIN
		IF EXISTS (SELECT * FROM HOCHAM WHERE MSHH = @MSHH) 
			INSERT INTO GIAOVIEN (MSGV, TENGV, SODT, DIACHI, MSHH, NAMHH) VALUES (@MSGV, @TENGV, @SODT, @DIACHI, @MSHH, @NAMHH)
		ELSE
			PRINT '0'
END

--2A Tham số vào là MSGV, TENGV, SODT, DIACHI, MSHH, NAMHH. Trước khi insert dữ liệu cần kiểm tra MSGV trong table GIAOVIEN có trùng không, nếu trùng thì trả về giá trị 0.
CREATE PROCEDURE THEM_GV2 @MSGV INT, @TENGV NVARCHAR(30), @SODT VARCHAR(10), @DIACHI NVARCHAR(50), @MSHH INT, @NAMHH SMALLDATETIME
AS
BEGIN
		IF EXISTS (SELECT * FROM GIAOVIEN WHERE MSGV = @MSGV) 
			INSERT INTO GIAOVIEN (MSGV, TENGV, SODT, DIACHI, MSHH, NAMHH) VALUES (@MSGV, @TENGV, @SODT, @DIACHI, @MSHH, @NAMHH)
		ELSE
			PRINT '0'
END

--3A Giống (1) và (2) kiểm tra xem MSGV có trùng không? MSHH có tồn tại chưa? Nếu MSGV trùng thì trả về 0. Nếu MSHH chưa tồn tại trả về 1, ngược lại cho insert dữ liệu
CREATE PROCEDURE THEM_GV3 @MSGV INT, @TENGV NVARCHAR(30), @SODT VARCHAR(10), @DIACHI NVARCHAR(50), @MSHH INT, @NAMHH SMALLDATETIME
AS
BEGIN
		IF EXISTS (SELECT * FROM GIAOVIEN WHERE MSGV = @MSGV) 
			PRINT '0'
		ELSE 
			IF NOT EXISTS (SELECT * FROM HOCHAM WHERE MSHH = @MSHH)
				PRINT '1'
			ELSE 
				INSERT INTO GIAOVIEN (MSGV, TENGV, SODT, DIACHI, MSHH, NAMHH) VALUES (@MSGV, @TENGV, @SODT, @DIACHI, @MSHH, @NAMHH)
END

--- B PROCEDURE CO THAM SO RA 

--1B Đưa vào TENHV trả ra: Số GV thỏa học vị, nếu không tìm thấy trả về 0.
CREATE PROCEDURE SO_GV_HV @TENHV NVARCHAR(20), @SLGV INT OUTPUT 
AS
BEGIN
	SELECT @SLGV = COUNT(MSGV)
	FROM GV_HV_CN JOIN HOCVI ON GV_HV_CN.MSHV = HOCVI.MSHV
	WHERE TENHV = @TENHV
END

--2B Đưa vào MSDT cho biết: Điểm trung bình của đề tài, nếu không tìm thấy trả về 0.
CREATE PROCEDURE TINH_DTB @MSDT CHAR(6), @DIEM FLOAT OUTPUT 
AS
BEGIN
	SELECT (SUM(GV_HDDT.DIEM) + SUM(GV_UVDT.DIEM) + SUM(GV_PBDT.DIEM)) / (COUNT(GV_HDDT.DIEM) + COUNT(GV_UVDT.DIEM) + COUNT(GV_PBDT.DIEM))
	FROM DETAI AS DT JOIN GV_HDDT ON DT.MSDT = GV_HDDT.MSDT
	JOIN GV_UVDT ON DT.MSDT = GV_UVDT.MSDT
	JOIN GV_PBDT ON DT.MSDT = GV_PBDT.MSDT
	WHERE DT.MSDT = @MSDT
	IF (@DIEM IS NULL)
		SET @DIEM = 0
END

