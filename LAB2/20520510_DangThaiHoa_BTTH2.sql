--1C Tạo Trigger thỏa mãn điều kiện khi xóa một đề tài sẽ xóa các thông tin liên quan.
CREATE TRIGGER DEL_DETAI
ON DETAI
INSTEAD OF DELETE 
AS 
BEGIN
	DECLARE @MSDT CHAR(6)
	SELECT @MSDT = MSDT FROM deleted
	DELETE FROM SV_DETAI
	WHERE MSDT = @MSDT
	DELETE FROM GV_HDDT
	WHERE MSDT = @MSDT
	DELETE FROM GV_PBDT
	WHERE MSDT = @MSDT
	DELETE FROM GV_UVDT
	WHERE MSDT = @MSDT
	DELETE FROM HOIDONG_DT
	WHERE MSDT = @MSDT
	DELETE FROM DETAI
	WHERE MSDT = @MSDT
END 

